#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*s1_cpy;
	unsigned char	*s2_cpy;

	s1_cpy = (unsigned char *)s1;
	s2_cpy = (unsigned char *)s2;
	i = 0;
	while (i < n && s1_cpy[i] == s2_cpy[i])
		i++;
	if (i == n)
		return (0);
	else
		return (s1_cpy[i] - s2_cpy[i]);
}
